// Generates web/generated/registry.mjs — the static app-class manifest for
// the browser bundle.
//
// The Node runtime discovers app classes by walking well-known directories
// (z2ui5_cl_util._app_dirs) and require()-ing files on demand. A bundler
// cannot resolve computed require paths, so the browser build needs the
// full class list at build time. This script produces it:
//
//   1. collect *.js class files from the framework built-ins
//      (srv/z2ui5/02, srv/z2ui5/02/01) and the whole samples tree
//      (srv/samples, recursive) — same order the runtime search uses,
//      first hit per class name wins
//   2. smoke-require every candidate in Node and skip files that fail to
//      load (parse errors, broken requires) — same policy as the sync
//      pipeline's copy step, which already skips non-parsing transpiles
//   3. emit an ESM module mapping class name → constructor
//
// Run directly (node gen-registry.mjs) or via build.mjs.

import fs from "node:fs";
import path from "node:path";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";
import { ROOT_DIR, CAP_DIR } from "./paths.mjs";

const OUT_FILE = path.join(ROOT_DIR, "generated", "registry.mjs");

// Modules that live in the app folders but are not app classes —
// z2ui5_cl_http_handler is the CDS action handler (and would pull @sap/cds
// into the smoke-require).
const EXCLUDE = new Set(["register-apps", "z2ui5_cl_http_handler"]);

// Same walk order as z2ui5_cl_util._walkClassFiles: top-level files first,
// then subdirectories — so a top-level class shadows a nested duplicate.
function walkClassFiles(dir, out = []) {
  if (!fs.existsSync(dir)) return out;
  const subdirs = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, entry.name);
    if (entry.isDirectory()) subdirs.push(p);
    else if (entry.name.endsWith(".js")) out.push(p);
  }
  for (const sub of subdirs) walkClassFiles(sub, out);
  return out;
}

export function generateRegistry({ excludeFiles = new Set() } = {}) {
  const candidates = [
    // 1. framework built-ins (startup app, hello world, popups) —
    //    non-recursive on 02/ so the core modules' subfolders stay out
    ...fs
      .readdirSync(path.join(CAP_DIR, "srv/z2ui5/02"))
      .filter((f) => f.endsWith(".js"))
      .map((f) => path.join(CAP_DIR, "srv/z2ui5/02", f)),
    ...walkClassFiles(path.join(CAP_DIR, "srv/z2ui5/02/01")),
    // 2. bundled samples, recursive (subfolders mirror upstream src/)
    ...walkClassFiles(path.join(CAP_DIR, "srv/samples")),
  ];

  // The require anchor sits inside the CAP package so the samples'
  // require("abap2UI5/...") self-references resolve via its package.json
  // exports map.
  const capRequire = createRequire(path.join(CAP_DIR, "package.json"));

  const entries = new Map(); // className → absolute file path
  const skipped = [];
  for (const file of candidates) {
    const className = path.basename(file, ".js");
    if (EXCLUDE.has(className) || entries.has(className)) continue;
    if (excludeFiles.has(file)) {
      skipped.push({ className, error: "rejected by esbuild in a previous bundle attempt" });
      continue;
    }
    try {
      const Cls = capRequire(file);
      if (typeof Cls !== "function") continue; // not a class module
      entries.set(className, file);
    } catch (e) {
      skipped.push({ className, error: e.message.split("\n")[0] });
    }
  }

  const lines = [
    "// AUTO-GENERATED by web/gen-registry.mjs — do not edit.",
    "// Maps class name → constructor for every app class the browser",
    "// bundle ships; web/entry.mjs registers them with z2ui5_cl_util.",
    "",
  ];
  const names = [...entries.keys()];
  names.forEach((name, i) => {
    const rel = path
      .relative(path.dirname(OUT_FILE), entries.get(name))
      .split(path.sep)
      .join("/");
    lines.push(`import cls_${i} from ${JSON.stringify(rel)};`);
  });
  lines.push("", "export default {");
  names.forEach((name, i) => {
    lines.push(`  ${JSON.stringify(name)}: cls_${i},`);
  });
  lines.push("};", "");

  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, lines.join("\n"));

  console.log(`registry: ${entries.size} classes → ${path.relative(process.cwd(), OUT_FILE)}`);
  if (skipped.length) {
    console.log(`registry: skipped ${skipped.length} module(s) that failed to load:`);
    for (const s of skipped) console.log(`  - ${s.className}: ${s.error}`);
  }
  return { count: entries.size, skipped, files: [...entries.values()] };
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  generateRegistry();
}
